    private void LlistaCaracteristiques() {
        final String Carpeta="" ;
        final String Fitxer="" ;
        final String Ocult="" ;
        final File seleccio = new File (ruta);
        final   Long v = seleccio.getFreeSpace();
        final   Long x = seleccio.getTotalSpace();
        final   Long z = seleccio.getUsableSpace();   
        final boolean carpeta = seleccio.isDirectory();
        final boolean fitxer = seleccio.isFile();
        final boolean ocult = seleccio.isHidden();
        final   Long y = seleccio.lastModified();
        final   Long w = seleccio.length();
jList1 = new javax.swing.JList();

jList1.setModel(new javax.swing.AbstractListModel() {
    String[] strings = { 
        "Ruta Absoluta del fitxer:" , seleccio.getAbsolutePath() , 
        "BYTES NO ASSIGNATS:", v.toString() , 
        "Nom de l'arxiu:", seleccio.getName() , 
        "Carpeta on es troba:" , seleccio.getParent() , 
        "Espai de la partició", x.toString() , 
        "Espai Utilitzable", z.toString() ,
        "És un directori?", Carpeta.valueOf(carpeta)  ,
        "És un fitxer?", Fitxer.valueOf(fitxer) ,
        "Està ocult?", Ocult.valueOf(ocult) ,
        "Ultima modificació", y.toString() ,
        "Mida del fitxer", w.toString() ,
        "Item 5", "Item 5"};
    public int getSize() { return strings.length; }
    public Object getElementAt(int i) { return strings[i]; }
});

jScrollPane1.setViewportView(jList1);

    
}
