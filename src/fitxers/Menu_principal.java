    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        
        
        JFileChooser jF1 = new JFileChooser();
        try {
            if (jF1.showSaveDialog(null) == jF1.APPROVE_OPTION) {
                ruta = jF1.getSelectedFile().getAbsolutePath();
               File seleccio = new File (ruta);
                
                jTextField2.setText(ruta);
                jCheckBox1.setSelected(seleccio.canExecute());
                jCheckBox4.setSelected(seleccio.canExecute());
                jCheckBox2.setSelected(seleccio.canRead());
                jCheckBox5.setSelected(seleccio.canRead());
                jCheckBox3.setSelected(seleccio.canWrite());
                jCheckBox6.setSelected(seleccio.canWrite());
                LlistaCaracteristiques();
            }
            
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
            
    }//GEN-LAST:event_jButton2MouseClicked

